# What are variables?
# A variable is like a box. We use it to store information. We can change what is inside the box anytime.

# Rules of variables :
# 1. Start with a letter or _
#       Example :  Correct → age, _score,  Wrong → 2age

# 2. No spaces
#       Example :  Correct → student_marks, Wrong → student marks

# 3. Small and capital letters are different
#       Example:  Age and age are NOT the same variable.

# 4. No special symbols
#       Example:  Correct → score1, Wrong → score@

