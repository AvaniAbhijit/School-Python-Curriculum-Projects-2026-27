# What is return?
# return sends the result back from the function to where it was called.

# Task 1 :  Create a function called multiply_numbers
#           It should multiply two numbers and return the answer.


def add_scores(score1, score2):   # Function with two parameters
    total = score1 + score2       # Add both numbers and store in total
    return total                  # Send the total back to where function was called


final_score = add_scores(10, 20)  # Calling the function and storing returned value
print("Total Score is:", final_score)   # Printing the final result


