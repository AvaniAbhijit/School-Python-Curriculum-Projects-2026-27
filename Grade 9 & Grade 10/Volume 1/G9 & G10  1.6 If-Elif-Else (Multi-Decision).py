# below code changes phone notification status based on location.

location = "school"

if location == "home":
    print("Normal Sound Mode")        # You can receive all notifications
elif location == "school":
    print("Silent Mode Activated")    # No disturbance in class
elif location == "meeting":
    print("Vibration Mode")           # Alerts without sound
else:
    print("Manual Mode")              # Unknown place
    






