# Relational Operators
print(5 < 10)   # True
print(8 > 15)   # False

# Logical Operators
# and operator - Returns True if BOTH conditions are True
age = 12
print(age > 10 and age < 15)   # True

# or operator - Returns True if ANY ONE condition is True
marks = 35
print(marks > 90 or marks > 30)   # True






