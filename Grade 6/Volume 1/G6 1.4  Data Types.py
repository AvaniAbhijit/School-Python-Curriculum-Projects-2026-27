# What are Data Types?
# Data types tell us what kind of data a variable is storing.
# Python uses different data types to store different kinds of values.

# Types of Data Types:

# 1. String
# Used to store text data.
# Text is always written inside quotes (" ").

# 2. Integer
# Used to store whole numbers.
# It does not contain decimal values.

# 3. Float
# Used to store decimal numbers.
# It contains a decimal point.

# 4. Boolean
# Used to store True or False values.
# The first letter must be capital (True / False).


# Task 1: Run the code and check the output.
# Task 2: Change the values of the variables.
# Task 3: Create one new variable for each data type.
# Task 4: Check the data type of all variables using type().


# String data type
student_name = "Riya"
print(student_name)

# Integer data type
student_age = 11
print(student_age)

# Float data type
percentage = 92.5
print(percentage)

# Boolean data type
is_student = True
print(is_student)


# Checking the data type of variables
print(type(student_name))
print(type(student_age))
print(type(percentage))
print(type(is_student))
